package com.yaser.domain;

public class Resulution {
	private int en;
	private int boy;

	public Resulution(int en, int boy) {
		this.en = en;
		this.boy = boy;
	}

	public int getEn() {
		return en;
	}

	public int getBoy() {
		return boy;
	}

	public void setEn(int en) {
		this.en = en;
	}

	public void setBoy(int boy) {
		this.boy = boy;
	}

}
